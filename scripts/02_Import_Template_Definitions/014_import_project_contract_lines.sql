	B.DEFAULTRATETABLEID,
	B.TSRATETABLEID,
	B.PORATETABLEID,
	B.APRATETABLEID,
	B.GLRATETABLEID,
	B.CCRATETABLEID,
	B.DEPARTMENTID,
	B.ENTRY_WFTYPE,
	B.ENTRY_PROJECTID,
	B.ENTRY_TASKID,
	B.ENTRY_COSTTYPEID,
	B.ENTRY_ITEMID,
	B.ENTRY_PRICE,
	B.ENTRY_PRICEEFFECTIVEDATE,
	B.ENTRY_MEMO,
	B.ENTRY_DEPARTMENTID,
	B.TASKID,
	B.COSTTYPEID,
	B.ENTRY_CUSTOMERID,
